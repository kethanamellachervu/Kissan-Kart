import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
};

serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    // Removed LOVABLE_API_KEY usage and replaced with placeholder or no auth
    const { prompt } = await req.json();

    // Replace the AI gateway URL with your own or a placeholder
    const response = await fetch('https://your-ai-api-endpoint.example.com/v1/chat/completions', {
      method: 'POST',
      headers: {
        // 'Authorization': `Bearer YOUR_API_KEY`, // Add your authorization header here
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        model: 'google/gemini-3-pro-image-preview',
        messages: [
          {
            role: 'user',
            content: prompt || 'Create a realistic digital illustration of Telangana farmers working in a field. Show both male and female farmers wearing traditional Telangana attire such as lungi/dhoti, cotton shirt, and saree with a towel on the shoulder. Include natural skin tone and facial features typical of rural Telangana people, with expressions showing confidence. Some farmers should be using a smartphone to access an e-commerce platform. Background should display crops like paddy or cotton with Telangana village sceneryâ€”huts, fields, and mild hills. Ensure no real person is identifiable, only generic representation.'
          }
        ],
        modalities: ['image', 'text']
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(
          JSON.stringify({ error: 'Rate limit exceeded. Please try again later.' }),
          { status: 429, headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
        );
      }
      if (response.status === 402) {
        return new Response(
          JSON.stringify({ error: 'Payment required. Please add credits to your workspace.' }),
          { status: 402, headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
        );
      }
      const errorText = await response.text();
      console.error('AI gateway error:', response.status, errorText);
      throw new Error(`AI gateway error: ${response.status}`);
    }

    const data = await response.json();
    const imageUrl = data.choices?.[0]?.message?.images?.[0]?.image_url?.url;

    if (!imageUrl) {
      throw new Error('No image generated');
    }

    return new Response(
      JSON.stringify({ imageUrl }),
      { headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
    );
  } catch (error) {
    console.error('Error generating image:', error);
    return new Response(
      JSON.stringify({ error: error instanceof Error ? error.message : 'Unknown error' }),
      { status: 500, headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
    );
  }
});
